        <h1 class="text-center text-[#4b83cc] font-semibold antialiased text-3xl pb-4">A Message From The President</h1>
        <p>Welcome to Divine Word College of Legazpi. It matters to us that you know who we are and why this institution exists and will continue to exist.</p>
        <br>
        <p>The raison d’être of Divine Word College of Legazpi reflects the vision of Saint Arnold Janssen, the founder of the religious-missionary congregation – the Society of the Divine Word (SVD). Education is among the missionary apostolates of the Society. Our Founder, a secondary school teacher himself teaching natural sciences and mathematics, understood the importance of education as a method for evangelization, and as an effective way of missionary service. He impressed upon his missionaries that an integral approach in education is critical in the faith-formation of the person. To this day, we continue to live his vision and ensure that integral formation of the human person takes place in our schools. Our aim is to inform and mold individuals into “persons of the Word”, hearers and doers of the Word – in the language of the Divine Word Educational Association, "witnesses to the Word in the world".</p>
        <br>
        <p>To honor this commitment, here at Divine Word College of Legazpi, we value excellence, service, and professionalism. We also ascribe to the values of justice, peace and integrity of creation as an acknowledgment of our common origin as created beings, and our interrelatedness with one another through mutual respect and co-responsibility – hence, our being a family. These values are at the core of our day-to-day undertaking – a way of life, a pledge we honor and desire to render to our students and other stakeholders.</p>
        <br>
        <p>I invite you then to learn more about us. As you move through the pages of our website you will discover that our academic programs are designed to provide life skills and competencies that enable our young people to navigate confidently in the constantly changing milieu of the 21st century. Our commitment to academic excellence and holistic development nurtured in a healthy organizational environment and permeated with a missionary paradigm, our sense of corporate social responsibility and best practices in community involvement are meant to prepare our young to succeed in life as competent professionals with a Christian missionary character – ready for the world.</p>
        <br>
        <p>I wish you to be part of our family here at Divine Word College of Legazpi. We are here to serve. We give you our word.</p>
        <br>
        <p>In the Divine Word,</p>
        <br>
        <p>REV. FR. NIELO M. CANTILADO, SVD
